package Pirmitives;

public class UnaryOperators {
    public static void main(String[] args) {
        System.out.println();


        int appleNumber=30;


        appleNumber++;; //apple number increase for 1

        System.out.println(appleNumber);

        appleNumber--; // apple number decreased for 1
        System.out.println(appleNumber);






    }


    }

